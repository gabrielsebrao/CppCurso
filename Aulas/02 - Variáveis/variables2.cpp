#include <iostream>

int main() {

    // Integer (whole numbers)
    int age = 18;
    int year = 2023;

    // Double (decimal numbers)
    double price = 19.99;
    double gpa = 2.5;

    // Char (single character)
    char at = '@';
    char initial = 'G';

    // Boolean (true or false)
    bool student = false;
    bool power = true;

    // String (an object (array of char) that represents a sequence of text)
    std::string name = "Gabriel";
    std::string dayWeek = "Friday";

    printf("Hello");

    return 0;
}